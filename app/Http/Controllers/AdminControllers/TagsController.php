     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $tag = Tag::firstOrCreate(['tag' => ucfirst(strtolower($request->tag))]);
        if(!$tag->courses->contains($request->course))
            $tag->courses()->attach($request->course);
        return 'ok';
    }

